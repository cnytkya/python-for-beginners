# int: tam sayı değerleri taşır
a = 10
print(f"int tipindeki a: {a}")
# float: ondalıklı sayı değerlerini taşır.
a = 15.5
print(f"float tipindeki a: {a}")

# str(string): text tipindeki değerleri taşır.
a = "Cüneyt Kaya"
print(f"str tipindeki a: {a}")

# list(liste): liste tipindeki değerleri taşır.
a = [12,13,14,15,16]
print(f"list tipindeki a: {a}")


